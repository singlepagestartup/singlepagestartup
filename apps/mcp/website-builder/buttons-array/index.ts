import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { api as websiteBuilderButtonsArrayApi } from "@sps/website-builder/models/buttons-array/sdk/server";
import { insertSchema as websiteBuilderButtonsArrayInsertSchema } from "@sps/website-builder/models/buttons-array/sdk/model";
import { RBAC_SECRET_KEY } from "@sps/shared-utils";

export function registerResources(mcp: McpServer) {
  mcp.registerResource(
    "website-builder-module-buttons-arrays",
    "sps://website-builder/buttons-arrays",
    {
      title: "website-builder module buttons-arrays",
      description: "Get list of all buttons-arrays from website-builder module",
    },
    async (uri) => {
      const resp = await websiteBuilderButtonsArrayApi.find();

      return {
        contents: [
          {
            uri: uri.href,
            text: JSON.stringify(resp, null, 2),
          },
        ],
      };
    },
  );
}

