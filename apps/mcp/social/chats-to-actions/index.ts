import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { api as socialChatsToActionsApi } from "@sps/social/relations/chats-to-actions/sdk/server";
import { insertSchema as socialChatsToActionsInsertSchema } from "@sps/social/relations/chats-to-actions/sdk/model";
import { RBAC_SECRET_KEY } from "@sps/shared-utils";

export function registerResources(mcp: McpServer) {
  mcp.registerResource(
    "social-relations-chats-to-actions",
    "sps://social/chats-to-actions",
    {
      title: "social chats-to-actions relation",
      description:
        "Get list of all chats-to-actions relations from social module",
    },
    async (uri) => {
      const resp = await socialChatsToActionsApi.find();

      return {
        contents: [
          {
            uri: uri.href,
            text: JSON.stringify(resp, null, 2),
          },
        ],
      };
    },
  );
}

