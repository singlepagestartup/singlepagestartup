import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { api as crmInputsToOptionsApi } from "@sps/crm/relations/inputs-to-options/sdk/server";
import { insertSchema as crmInputsToOptionsInsertSchema } from "@sps/crm/relations/inputs-to-options/sdk/model";
import { RBAC_SECRET_KEY } from "@sps/shared-utils";

export function registerResources(mcp: McpServer) {
  mcp.registerResource(
    "crm-relations-inputs-to-options",
    "sps://crm/inputs-to-options",
    {
      title: "crm inputs-to-options relation",
      description:
        "Get list of all inputs-to-options relations from crm module",
    },
    async (uri) => {
      const resp = await crmInputsToOptionsApi.find();

      return {
        contents: [
          {
            uri: uri.href,
            text: JSON.stringify(resp, null, 2),
          },
        ],
      };
    },
  );
}

