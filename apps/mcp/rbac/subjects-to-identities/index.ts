import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { api as rbacSubjectsToIdentitiesApi } from "@sps/rbac/relations/subjects-to-identities/sdk/server";
import { insertSchema as rbacSubjectsToIdentitiesInsertSchema } from "@sps/rbac/relations/subjects-to-identities/sdk/model";
import { RBAC_SECRET_KEY } from "@sps/shared-utils";

export function registerResources(mcp: McpServer) {
  mcp.registerResource(
    "rbac-relations-subjects-to-identities",
    "sps://rbac/subjects-to-identities",
    {
      title: "rbac subjects-to-identities relation",
      description:
        "Get list of all subjects-to-identities relations from rbac module",
    },
    async (uri) => {
      const resp = await rbacSubjectsToIdentitiesApi.find();

      return {
        contents: [
          {
            uri: uri.href,
            text: JSON.stringify(resp, null, 2),
          },
        ],
      };
    },
  );
}

