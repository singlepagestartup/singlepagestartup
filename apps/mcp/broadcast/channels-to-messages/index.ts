import { RBAC_SECRET_KEY } from "@sps/shared-utils";

export function registerResources(mcp: McpServer) {
  mcp.registerResource(
    "broadcast-relations-channels-to-messages",
    "sps://broadcast/channels-to-messages",
    {
      title: "broadcast channels-to-messages relation",
      description:
        "Get list of all channels-to-messages relations from broadcast module",
    },
    async (uri) => {
      const resp = await broadcastChannelsToMessagesApi.find();

      return {
        contents: [
          {
            uri: uri.href,
            text: JSON.stringify(resp, null, 2),
          },
        ],
      };
    },
  );
}

