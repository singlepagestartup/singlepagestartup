import { McpServer } from "@modelcontextprotocol/sdk/server/mcp.js";
import { api as ecommerceStoresToProductsApi } from "@sps/ecommerce/relations/stores-to-products/sdk/server";
import { insertSchema as ecommerceStoresToProductsInsertSchema } from "@sps/ecommerce/relations/stores-to-products/sdk/model";
import { RBAC_SECRET_KEY } from "@sps/shared-utils";

export function registerResources(mcp: McpServer) {
  mcp.registerResource(
    "ecommerce-relations-stores-to-products",
    "sps://ecommerce/stores-to-products",
    {
      title: "ecommerce stores-to-products relation",
      description:
        "Get list of all stores-to-products relations from ecommerce module",
    },
    async (uri) => {
      const resp = await ecommerceStoresToProductsApi.find();

      return {
        contents: [
          {
            uri: uri.href,
            text: JSON.stringify(resp, null, 2),
          },
        ],
      };
    },
  );
}

