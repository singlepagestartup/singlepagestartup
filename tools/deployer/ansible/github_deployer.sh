#!/bin/bash

. ./get_env.sh

ANSIBLE_HOST=$(get_env ANSIBLE_HOST)
ANSIBLE_PASSWORD=$(get_env ANSIBLE_PASSWORD)

ROOT_AWS_ACCESS_KEY_ID=$(get_env ROOT_AWS_ACCESS_KEY_ID)
ROOT_AWS_SECRET_ACCESS_KEY=$(get_env ROOT_AWS_SECRET_ACCESS_KEY)
AWS_S3_BUCKET=$(get_env AWS_S3_BUCKET)
AWS_REGION=$(get_env AWS_REGION)

ROOT_CLOUDFLARE_ACCOUNT_API_KEY=$(get_env ROOT_CLOUDFLARE_ACCOUNT_API_KEY)
ROOT_CLOUDFLARE_ACCOUNT_EMAIL=$(get_env ROOT_CLOUDFLARE_ACCOUNT_EMAIL)

ROOT_SENTRY_API_KEY=$(get_env ROOT_SENTRY_API_KEY)
ROOT_SENTRY_ORGANIZATION_SLUG=$(get_env ROOT_SENTRY_ORGANIZATION_SLUG)
ROOT_SENTRY_TEAM_SLUG=$(get_env ROOT_SENTRY_TEAM_SLUG)

DOMAIN=$(get_env DOMAIN)

RBAC_JWT_SECRET=$(get_env RBAC_JWT_SECRET)
RBAC_SECRET_KEY=$(get_env RBAC_SECRET_KEY)
RBAC_COOKIE_SESSION_SECRET=$(get_env RBAC_COOKIE_SESSION_SECRET)

DATABASE_NAME=$(get_env DATABASE_NAME)
DATABASE_USERNAME=$(get_env DATABASE_USERNAME)
DATABASE_PASSWORD=$(get_env DATABASE_PASSWORD)

REDIS_PASSWORD=$(get_env REDIS_PASSWORD)
REDIS_PORT=$(get_env REDIS_PORT)

PORTAINER_USERNAME=$(get_env PORTAINER_USERNAME)
PORTAINER_PASSWORD=$(get_env PORTAINER_PASSWORD)
PORTAINER_SERVICE_SUBDOMAIN=$(get_env PORTAINER_SERVICE_SUBDOMAIN)

TRAEFIK_USERNAME=$(get_env TRAEFIK_USERNAME)
TRAEFIK_PASSWORD=$(get_env TRAEFIK_PASSWORD)
TRAEFIK_SERVICE_SUBDOMAIN=$(get_env TRAEFIK_SERVICE_SUBDOMAIN)

BACKEND_SERVICE_NAME=$(get_env BACKEND_SERVICE_NAME)
BACKEND_SERVICE_SUBDOMAIN=$(get_env BACKEND_SERVICE_SUBDOMAIN)
BACKEND_DOCKER_HUB_REPOSITORY_NAME=$(get_env BACKEND_DOCKER_HUB_REPOSITORY_NAME)

HOST_SERVICE_NAME=$(get_env HOST_SERVICE_NAME)
HOST_SERVICE_SUBDOMAIN=$(get_env HOST_SERVICE_SUBDOMAIN)
HOST_DOCKER_HUB_REPOSITORY_NAME=$(get_env HOST_DOCKER_HUB_REPOSITORY_NAME)

GITHUB_TOKEN=$(get_env GITHUB_TOKEN)
GITHUB_REPOSITORY=$(get_env GITHUB_REPOSITORY)

STRIPE_SECRET_KEY=$(get_env STRIPE_SECRET_KEY)

O_X_PROCESSING_TEST_PAYMENTS=$(get_env O_X_PROCESSING_TEST_PAYMENTS)
O_X_PROCESSING_SHOP_ID=$(get_env O_X_PROCESSING_SHOP_ID)
O_X_PROCESSING_WEBHOOK_PASSWORD=$(get_env O_X_PROCESSING_WEBHOOK_PASSWORD)

PAYSELECTION_SECRET_KEY=$(get_env PAYSELECTION_SECRET_KEY)
PAYSELECTION_SITE_ID=$(get_env PAYSELECTION_SITE_ID)
PAYSELECTION_SITE_NAME=$(get_env PAYSELECTION_SITE_NAME)
PAYSELECTION_PUBLIC_KEY=$(get_env PAYSELECTION_PUBLIC_KEY)
PAYSELECTION_WEBHOOK_URL=$(get_env PAYSELECTION_WEBHOOK_URL)

GOOGLE_TAG_MANAGER_ID=$(get_env GOOGLE_TAG_MANAGER_ID)
GOOGLE_ANALYTICS_ID=$(get_env GOOGLE_ANALYTICS_ID)

SECRETS=(\
    "ANSIBLE_HOST $ANSIBLE_HOST" \
    "ANSIBLE_PASSWORD $ANSIBLE_PASSWORD" \
    "ROOT_AWS_ACCESS_KEY_ID $ROOT_AWS_ACCESS_KEY_ID" \
    "ROOT_AWS_SECRET_ACCESS_KEY $ROOT_AWS_SECRET_ACCESS_KEY" \
    "ROOT_CLOUDFLARE_ACCOUNT_API_KEY $ROOT_CLOUDFLARE_ACCOUNT_API_KEY" \
    "ROOT_CLOUDFLARE_ACCOUNT_EMAIL $ROOT_CLOUDFLARE_ACCOUNT_EMAIL" \
    "ROOT_SENTRY_API_KEY $ROOT_SENTRY_API_KEY" \
    "ROOT_SENTRY_ORGANIZATION_SLUG $ROOT_SENTRY_ORGANIZATION_SLUG" \
    "ROOT_SENTRY_TEAM_SLUG $ROOT_SENTRY_TEAM_SLUG" \
    "DOMAIN $DOMAIN" \
    "RBAC_JWT_SECRET $RBAC_JWT_SECRET" \
    "RBAC_SECRET_KEY $RBAC_SECRET_KEY" \
    "RBAC_COOKIE_SESSION_SECRET $RBAC_COOKIE_SESSION_SECRET" \
    "AWS_S3_BUCKET $AWS_S3_BUCKET" \
    "AWS_REGION $AWS_REGION" \
    "DATABASE_NAME $DATABASE_NAME" \
    "DATABASE_USERNAME $DATABASE_USERNAME" \
    "DATABASE_PASSWORD $DATABASE_PASSWORD" \
    "REDIS_PASSWORD $REDIS_PASSWORD" \
    "REDIS_PORT $REDIS_PORT" \
    "PORTAINER_USERNAME $PORTAINER_USERNAME" \
    "PORTAINER_PASSWORD $PORTAINER_PASSWORD" \
    "PORTAINER_SERVICE_SUBDOMAIN $PORTAINER_SERVICE_SUBDOMAIN" \
    "TRAEFIK_USERNAME $TRAEFIK_USERNAME" \
    "TRAEFIK_PASSWORD $TRAEFIK_PASSWORD" \
    "TRAEFIK_SERVICE_SUBDOMAIN $TRAEFIK_SERVICE_SUBDOMAIN" \
    "BACKEND_SERVICE_NAME $BACKEND_SERVICE_NAME" \
    "BACKEND_SERVICE_SUBDOMAIN $BACKEND_SERVICE_SUBDOMAIN" \
    "BACKEND_DOCKER_HUB_REPOSITORY_NAME $BACKEND_DOCKER_HUB_REPOSITORY_NAME" \
    "HOST_SERVICE_NAME $HOST_SERVICE_NAME" \
    "HOST_SERVICE_SUBDOMAIN $HOST_SERVICE_SUBDOMAIN" \
    "HOST_DOCKER_HUB_REPOSITORY_NAME $HOST_DOCKER_HUB_REPOSITORY_NAME" \
    "GH_TOKEN $GITHUB_TOKEN" \
    "STRIPE_SECRET_KEY $STRIPE_SECRET_KEY" \
    "O_X_PROCESSING_TEST_PAYMENTS $O_X_PROCESSING_TEST_PAYMENTS" \
    "O_X_PROCESSING_SHOP_ID $O_X_PROCESSING_SHOP_ID" \
    "O_X_PROCESSING_WEBHOOK_PASSWORD $O_X_PROCESSING_WEBHOOK_PASSWORD" \
    "PAYSELECTION_SECRET_KEY $PAYSELECTION_SECRET_KEY" \
    "PAYSELECTION_SITE_ID $PAYSELECTION_SITE_ID" \
    "PAYSELECTION_SITE_NAME $PAYSELECTION_SITE_NAME" \
    "PAYSELECTION_PUBLIC_KEY $PAYSELECTION_PUBLIC_KEY" \
    "PAYSELECTION_WEBHOOK_URL $PAYSELECTION_WEBHOOK_URL" \
    "GOOGLE_TAG_MANAGER_ID $GOOGLE_TAG_MANAGER_ID" \
    "GOOGLE_ANALYTICS_ID $GOOGLE_ANALYTICS_ID" \
)

BRANCHES=( \
    "ansible-up" \
    "ansible-down" \
)

if [ "$1" != "down" ]
then
    cd ./github/github-node-api && npm install && cd ../..

    for i in "${BRANCHES[@]}"
    do
        BRANCH_NAME=$i
        node ./github/github-node-api/create_branch.js \
            $GITHUB_TOKEN \
            $GITHUB_REPOSITORY \
            $BRANCH_NAME
    done
        
    for i in "${SECRETS[@]}"
    do
        SECRET_NAME="${i%% *}"
        SECRET_CONTENT="${i#* }"

        if [ ! -z $SECRET_CONTENT ]
        then
            node ./github/github-node-api/create_secret.js \
                $GITHUB_TOKEN \
                $GITHUB_REPOSITORY \
                $SECRET_NAME \
                $SECRET_CONTENT
        fi
    done
else
    cd ./github/github-node-api && npm install && cd ../..

    for i in "${SECRETS[@]}"
    do
        SECRET_NAME="${i%% *}"
        SECRET_CONTENT="${i#* }"

        node ./github/github-node-api/delete_secret.js \
            $GITHUB_TOKEN \
            $GITHUB_REPOSITORY \
            $SECRET_NAME
    done
fi